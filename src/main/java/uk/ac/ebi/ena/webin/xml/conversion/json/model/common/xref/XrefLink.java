import lombok.Data;

@JsonPropertyOrder({"db", "id"})
@JsonIgnoreProperties(ignoreUnknown = true)
@Data
public class XrefLink {
  @JacksonXmlProperty(localName = "DB")
  private String db;

  @JacksonXmlProperty(localName = "ID")
  private String id;
}
